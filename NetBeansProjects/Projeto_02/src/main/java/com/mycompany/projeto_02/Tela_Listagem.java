    public static void preencherTabela(List<funcionario> f){
        String[] colunas = {"Id", "Tipo Pagamento", "Horista Id", "Assalariado Id", "Setor"};
        String[][] dados = new String[f.size()][colunas.length];
        
        int i=0;
        for(funcionario r: f){
            dados[i]= new String[]{
              String.valueOf(r.getId()),
              r.getTipoPagamento(),
              String.valueOf(r.getHorista_id()),
              String.valueOf(r.getAssalariado_id()),
              r.getSetor()
            };
            i++;
        }
        DefaultTableModel tabelaModelo = new DefaultTableModel(dados, colunas);
        Tabela1.setModel(tabelaModelo);
    }
    
    public Tela_Listagem() {
        initComponents();
        FuncionarioDAO fdao = new FuncionarioDAO();
        preencherTabela(fdao.listar());
    }

