    static void Verificacao(int posPaciente){
     
     List<Codigo> listaCompleto = new ArrayList();
     listaCompleto = Lista_Consulta.Listar();
     
     Codigo paciente = listaCompleto.get(posPaciente);
     String variavel="";
     variavel = paciente.getConRealizada();
     
     if(variavel.equals("Consulta Realizada")){
         
         Finalizar_Con.CheckBox_ConRealizada.setSelected(true);
         Finalizar_Con.CheckBox_ConRealizada.enable(false);
         Finalizar_Con.Botao_Finalizar.enable(false);
     }  
}
    
    
private int getPosicaoPaciente(){
     
     int posPaciente = Tabela2.getSelectedRow();
     
     
     if(posPaciente == -1){
         JOptionPane.showMessageDialog(rootPane, "Selecione um aluno");  
     }
     
     return posPaciente;
  }

