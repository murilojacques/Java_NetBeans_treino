        int f = sldFim.getValue();
        int p = sldPasso.getValue();
        
        DefaultListModel lista = new DefaultListModel();
        
        for(int c =i; c<=f; c += p){
            lista.addElement(c);
        }
        Lista1.setModel(lista);
