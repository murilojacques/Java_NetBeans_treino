    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
       Painel1.setVisible(true);
       
       int A = Integer.parseInt(spnA.getValue().toString());
       int B = Integer.parseInt(spnB.getValue().toString());
       int C = Integer.parseInt(spnC.getValue().toString());
       double delta = Math.pow(B, 2) - (4*A*C);
       
       lblResultado.setText(String.format("%.1f", delta));
       
       if(delta < 0){
           lblTipoRaiz.setText("NÃ£o existem Raizes Reais");
       }else{
           lblTipoRaiz.setText("Existem Raizes Reais");
       }
    }//GEN-LAST:event_btnCalcularActionPerformed

