    private void Botao_CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Botao_CalcularActionPerformed
        int valor = Integer.parseInt(Spn_Numero.getValue().toString());
        
        
        int dd = valor%2;
        R_DivisaoPDois.setText(Integer.toString(dd));
        
        
        double ec = Math.pow(valor, 3);
        R_ElvadoCubo.setText(Double.toString(ec));
        
        
        double rq = Math.sqrt(valor);
        R_RaizQuadrada.setText(String.format("%.2f", rq));
        
        
        double rc = Math.cbrt(valor);
        R_RaizCubica.setText(String.format("%.2f", rc));
        
        
        int va = Math.abs(valor);
        R_ValorAbs.setText(Integer.toString(va));
        
        
         PanCalc.setVisible(true);
    }//GEN-LAST:event_Botao_CalcularActionPerformed

